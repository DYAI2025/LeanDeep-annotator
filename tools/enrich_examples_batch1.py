def main():
    total_enriched = 0
    for marker_id, examples in EXAMPLES.items():
        filepath = find_file(marker_id)
        if filepath is None:
            print(f"SKIP {marker_id}: file not found")
            continue

        pos_count, neg_count = current_counts(
            yaml.load(open(filepath))
        )
        print(f"{marker_id}: before={pos_count}p/{neg_count}n", end="")

        p, n = enrich_marker(filepath, examples["positive"], examples["negative"])
        print(f" -> after={p}p/{n}n")
        total_enriched += 1

    print(f"\nEnriched {total_enriched} markers in batch 1.")


if __name__ == "__main__":
    main()
