"""Batch enrich ATO markers with examples - Batch 1 (first ~20 markers)."""

import os
import sys
from pathlib import Path
from ruamel.yaml import YAML

yaml = YAML()
yaml.preserve_quotes = True
yaml.width = 4096
yaml.default_flow_style = False

BASE = Path("/Users/benjaminpoersch/Projects/LeanDeep-annotator/build/markers_rated")
DIRS = [BASE / "1_approved" / "ATO", BASE / "2_good" / "ATO"]

def find_file(marker_id):
    for d in DIRS:
        p = d / f"{marker_id}.yaml"
        if p.exists():
            return p
    return None

def get_example_keys(data):
    """Detect whether marker uses positive/negative or positive_de/negative_de."""
    ex = data.get("examples", {})
    if ex is None:
        return None, None
    if "positive_de" in ex:
        return "positive_de", "negative_de"
    return "positive", "negative"

def current_counts(data):
    ex = data.get("examples", {})
    if ex is None:
        return 0, 0
    pk, nk = get_example_keys(data)
    if pk is None:
        return 0, 0
    pos = ex.get(pk, []) or []
    neg = ex.get(nk, []) or []
    return len(pos), len(neg)

def enrich_marker(filepath, new_positives, new_negatives):
    """Add examples to a marker file, reaching target counts."""
    with open(filepath) as f:
        data = yaml.load(f)

    if "examples" not in data or data["examples"] is None:
        data["examples"] = {}

    pk, nk = get_example_keys(data)
    if pk is None:
        pk, nk = "positive", "negative"

    existing_pos = list(data["examples"].get(pk, []) or [])
    existing_neg = list(data["examples"].get(nk, []) or [])

    # Deduplicate by adding only new ones
    pos_set = set(existing_pos)
    neg_set = set(existing_neg)

    for ex in new_positives:
        if ex not in pos_set and len(existing_pos) < 50:
            existing_pos.append(ex)
            pos_set.add(ex)

    for ex in new_negatives:
        if ex not in neg_set and len(existing_neg) < 25:
            existing_neg.append(ex)
            neg_set.add(ex)

    data["examples"][pk] = existing_pos
    data["examples"][nk] = existing_neg

    with open(filepath, "w") as f:
        yaml.dump(data, f)

    return len(existing_pos), len(existing_neg)


