    for marker_id in sorted(EXAMPLES.keys()):
        path = find_marker_file(marker_id)
        if path is None:
            continue
        import yaml as pyyaml
        with open(path, "r", encoding="utf-8") as f:
            data = pyyaml.safe_load(f)
        if data is None:
            continue
        ex = data.get("examples", {})
        pos = len(ex.get("positive", ex.get("positive_de", [])) or [])
        neg = len(ex.get("negative", ex.get("negative_de", [])) or [])
        if pos >= TARGET_POS and neg >= TARGET_NEG:
            at_target += 1
            print(f"  ‚úì {marker_id}: {pos} pos / {neg} neg")
        else:
            below_target += 1
            print(f"  ‚úó {marker_id}: {pos} pos / {neg} neg ‚Äî BELOW TARGET")

    print(f"\nAt target: {at_target} / {at_target + below_target}")


