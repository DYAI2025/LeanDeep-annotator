def find_marker_file(marker_id: str) -> Path | None:
    """Find the YAML file for a marker across all rating directories."""
    for d in DIRS:
        p = BASE / d / "ATO" / f"{marker_id}.yaml"
        if p.exists():
            return p
    return None


def detect_example_keys(examples: dict) -> tuple[str, str]:
    """Detect whether examples use positive/negative or positive_de/negative_de keys."""
    pos_key = "positive_de" if "positive_de" in examples else "positive"
    neg_key = "negative_de" if "negative_de" in examples else "negative"
    return pos_key, neg_key


def enrich_marker(marker_id: str) -> dict:
    """Enrich a single marker with examples. Returns status dict."""
    path = find_marker_file(marker_id)
    if path is None:
        return {"id": marker_id, "status": "skipped", "reason": "file not found"}

    # Load YAML
    with open(path, "r", encoding="utf-8") as f:
        data = yaml.load(f)

    if data is None:
        return {"id": marker_id, "status": "skipped", "reason": "empty YAML"}

    # Get or create examples section
    if "examples" not in data:
        data["examples"] = {}

    examples = data["examples"]
    pos_key, neg_key = detect_example_keys(examples)

    # Check if new examples use _de keys
    new_examples = EXAMPLES.get(marker_id, {})
    if not new_examples:
        return {"id": marker_id, "status": "skipped", "reason": "no examples in script"}

    # Determine the actual keys from our EXAMPLES dict
    if "positive_de" in new_examples:
        src_pos_key = "positive_de"
        src_neg_key = "negative_de"
        # Override the file keys to match
        pos_key = "positive_de"
        neg_key = "negative_de"
    else:
        src_pos_key = "positive"
        src_neg_key = "negative"

    # Current counts
    current_pos = examples.get(pos_key, []) or []
    current_neg = examples.get(neg_key, []) or []

    if not isinstance(current_pos, list):
        current_pos = []
    if not isinstance(current_neg, list):
        current_neg = []

    pos_count = len(current_pos)
    neg_count = len(current_neg)

    # Skip if already at target
    if pos_count >= TARGET_POS and neg_count >= TARGET_NEG:
        return {
            "id": marker_id,
            "status": "already_done",
            "pos": pos_count,
            "neg": neg_count,
        }

    # Get new examples
    new_pos = new_examples.get(src_pos_key, [])
    new_neg = new_examples.get(src_neg_key, [])

    # Merge: keep existing, add new until target reached
    existing_pos_set = set(current_pos)
    existing_neg_set = set(current_neg)

    # Add new positives (deduplicated)
    for ex in new_pos:
        if ex not in existing_pos_set and len(current_pos) < TARGET_POS:
            current_pos.append(ex)
            existing_pos_set.add(ex)

    # Add new negatives (deduplicated)
    for ex in new_neg:
        if ex not in existing_neg_set and len(current_neg) < TARGET_NEG:
            current_neg.append(ex)
            existing_neg_set.add(ex)

    # Update examples in data
    examples[pos_key] = current_pos
    examples[neg_key] = current_neg
    data["examples"] = examples

    # Write back
    with open(path, "w", encoding="utf-8") as f:
        yaml.dump(data, f)

    return {
        "id": marker_id,
        "status": "enriched",
        "pos": len(current_pos),
        "neg": len(current_neg),
        "pos_added": len(current_pos) - pos_count,
        "neg_added": len(current_neg) - neg_count,
        "path": str(path),
    }


